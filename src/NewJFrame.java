    public void akar(int bilangan1){
    double hasil = 0;
    hasil = Math.sqrt(bilangan1);
    texthasil.setText(hasil + "");
    }
    public void kuad(int bilangan1, int bilangan2){
     int hasil = 0;
    hasil = (int) Math.pow(bilangan1, bilangan2);
    texthasil.setText(hasil+"");
    }
    public void modulus(int bilangan1,int bilangan2){
    int hasil = 0;
    hasil = bilangan1 % bilangan2;
    texthasil.setText(hasil+"");
    }
    public void jumlah(int bilangan1,int bilangan2){
    int hasil = 0;
    hasil = bilangan1 + bilangan2;
    texthasil.setText(hasil+"");
    }
    public void kurang(int bilangan1,int bilangan2){
    int hasil = 0;
    hasil = bilangan1 - bilangan2;
    texthasil.setText(hasil+"");
    }
    public void bagi(int bilangan1,int bilangan2){
    double hasil = 0;
    hasil = bilangan1 / bilangan2;
    texthasil.setText(hasil+"");
    }
    public void kali(int bilangan1,int bilangan2){
    int hasil = 0;
    hasil = bilangan1 * bilangan2;
    texthasil.setText(hasil+"");
    }
